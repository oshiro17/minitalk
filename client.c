#include "minitalk.h"

int	main(int argc, char const *argv[])
{
	char		c;
	int			i;
	int			j;

	i = 0;
	if (argc != 3)
		return (1);
	if (kill(ft_atoi(argv[1]), 0) == -1)
		return (1);
	while (argv[2][i])
	{
		j = 0;
		c = argv[2][i];
		while (j < 8)
		{
			if (c >> (7 - j) & 1)
				kill(ft_atoi(argv[1]), SIGUSR1);
			//実行中のプログラムにシグナルを送る
			else
				kill(ft_atoi(argv[1]), SIGUSR2);
			j++;
			usleep(1000);
			
		}
		i++;
	}
	return (0);
}

